/************
파일명 : Or02Number.sql
숫자(수학) 관련 함수
설명 : 숫자데이터를 처리하기 위한 숫자관련 함수를 알아보자
    테이블 생성시 number 타입으로 선언된 컬럼에 저장된 데이터를
    대상으로 한다. 
*************/

/*
DUAL 테이블
    : 한 행으로 결과를 출력하기 위해 제공되는 테이블로 오라클에서 
    자동으로 생성되는 임시 테이블이다. 
    varchar2(1)로 정의된 dummy라는 단 하나의 컬럼으로 구성되어
    있다. 
*/
desc dual; -- 테이블의 레이아웃 확인
select * from dual;

--abs() : 절대값 구하기
select abs(-1000) AS "-1000의절대값" from dual ;
select abs(9000) AS "9000의절대값" from dual ;

/*
trunc() :  소수점을 특정자리수에서 잘라낼때 사용하는 함수
    형식 : trunc(컬럼명 혹은 값, 소수점이하자리수)
        두번째 인자가
            양수일때 : 주어진 숫자만큼 소수점을 표혐
            없을때 : 정수부만 표현. 즉 소수점 아래부분은 버림
            음수일때 : 정수부를 숫자만큼 잘라 나머지를 0으로 채움
*/
select trunc(1234.123456, 2) "TRUNC양수인자" from dual;
select trunc(1234.123456) "TRUNC인자없음" from dual;
select trunc(1234.123456, -2) "TRUNC음수인자" from dual;

--사원테이블에서 보너스율이 있는 사원만 가져와서 소수점 1자리로 표현하시오.
select 
    first_name, last_name, commission_pct, trunc(commission_pct, 1)
from employees
where commission_pct is not null;

/*
소수점 관련함수

ceil(값) : 소수점 이하를 무조건 올림처리

floor(값) : 소수점 이하를 무조건 버림처리.

round(값, 자리수) : 소수점 이하를 반올림 처리
    두번째 인자가
        없는경우 : 소수점 첫번째 자리가 5이상이면 올림, 미만이면 버림
        있는경우 : 숫자만큼 소수점이 표현되므로 그 다음수가 5이상이면
            올림, 미만이면 버림
*/
select ceil(32.8) from dual; --33
select ceil(32.3) from dual; --33

select floor(32.8) from dual; --32
select floor(32.3) from dual; --32

select round(0.123), round(0.543) from dual; -- 0, 1
--첫번째 : 소수이하 6자리까지 표현하므로 7을 올림처리함
--두번째 : 소수이하 4자리만 표현하므로 1을 버림처리함
select round(0.1234567, 6), round(2.345612, 4) from dual;


/*
power() : 거듭제곱을 계산할때 사용하는 함수
    사용법 : power(k, n) => k를 n번 곱한결과를 반환한다. 
*/
select power(4, 3) "4의3승" from dual;
select power(2, 10) "2의10승" from dual;

/*
mod() : 나머지를 구하는 함수
*/
select mod(99, 4) "99를 4로 나눈 나머지" from dual;

/*
sqrt() : 제곱근(루트)을 구하는 함수
    => 어떤수를 제곱하면 64가 되나요? => 64의 제곱근은?
*/
select sqrt(64), sqrt(9) "9의제곱근" from dual;

/*
연습문제] 3의 제곱근을 소수점 둘째자리까지 표현하시오.
단 trunc()함수와 round()함수를 사용해서 각각 구현하시오.
*/
select sqrt(3) "3의제곱근" from dual;
select trunc(sqrt(3), 2) from dual;
select round(sqrt(3), 2) from dual;
-- trunc()는 4자리 아래를 버림처리하고 round()는 반올림 처리함.
select trunc(sqrt(3), 4), round(sqrt(3), 4) from dual;













